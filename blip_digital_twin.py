client_engines = {}

@sock.route("/battery-stream")
def battery_stream(ws):
    client_id = id(ws)
    try:
        engine = None
        while True:
            # Receive next message from websocket
            message = ws.receive()
            if message is None:
                break
            try:
                data = json.loads(message)
            except Exception as e:
                ws.send(json.dumps({
                    "error": f"Invalid JSON: {str(e)}"
                }))
                continue

            # Optional close message from client
            if data.get("action") == "close":
                ws.send(json.dumps({"status": "Connection closed"}))
                break
            try:
                validate_json(data)
                # Create engine only once. Maintain state across messages

                if engine is None:
                    battery_type = data.get(
                        "battery_type",
                        "Others"
                    )
                    initial_soh = float(
                        data.get(
                            "initial_soh",
                            100
                        )
                    )
                    engine = BatteryDegradationEngine(
                        battery_type=battery_type,
                        initial_soh=initial_soh
                    )
                    client_engines[client_id] = engine
                output = engine.process(data)
                ws.send(json.dumps(output))

            except Exception as e:
                ws.send(json.dumps({"error": str(e)}))

    finally:
        # Cleanup after disconnect
        if client_id in client_engines:
            del client_engines[client_id]
        try:
            ws.close()
        except:
            pass

