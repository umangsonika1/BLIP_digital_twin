def calculate_soh():
    try:
        if "battery_data" not in request.files:
            return jsonify({
                "error":
                "'battery_data' JSON file required"

            }),400

        file=request.files["battery_data"]
        if not file.filename.lower().endswith(".json"):
            return jsonify({
                "error":
                "Only JSON allowed"
            }),400
        records=json.load(file)
        if not isinstance(records,list):
            records=[records]

        results=[]
        first=records[0]
        validate_json(first)
        engine=BatteryDegradationEngine(
            battery_type=first.get("battery_type","Others"),
            initial_soh=float(first.get("initial_soh",100))
        )

        for row in records:
            validate_json(row)
            output=engine.process(row)
            results.append(output)
        result_bytes=json.dumps(results,indent=2).encode("utf-8")

        return send_file(
            io.BytesIO(
                result_bytes
            ),
            as_attachment=True,
            download_name=
            "battery_soh_results.json",
            mimetype=
            "application/json"
        )
    except Exception as e:
        return jsonify({"error":str(e)}),500

